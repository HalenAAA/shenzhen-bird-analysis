def get_inaturalist_photo_url(species, df):
    """从 iNaturalist 数据里找一张照片"""
    if df is None or df.empty:
        return None
    inat = df[df.get("source", "") == "iNaturalist"] if "source" in df else pd.DataFrame()
    if inat.empty or "photo_url" not in inat.columns:
        return None
    photos = inat["photo_url"].dropna()
    if not photos.empty:
        return photos.iloc[0]
    return None
